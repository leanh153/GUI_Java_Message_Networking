    private void loadProfileImage() {

        FileHelper fHelper = new FileHelper((new File("img"))
            .getAbsolutePath());

        try {
            if (fHelper.isExistFileInDir()) {
                String iconPath = fHelper.getFile();
                lbIcon.setIcon(new ImageIcon(new ImageIcon(iconPath).getImage()
                    .getScaledInstance(30, 30, Image.SCALE_SMOOTH)));
            }
        } catch (IOException ex) {
            Logger.getLogger(ClientBox.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

