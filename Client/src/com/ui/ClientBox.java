    private void btnConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectActionPerformed
        // TODO add your handling code here:
        /*insert code for open a connection to server here*/
        if (clientThread == null) {
            try {
                Server server = new Server(txtHostIP.getText(), Integer.valueOf(txtPort.getText()));
                clientThread = new ClientThread(server, txtContent);
                // send information of user to server and save infomation of this user
                // to database

                String from = txtUsername.getText();
                String to = "Server";
                String content = txtMessage.getText();
                Message msg = new Message(from, to, new Date(), content, MessageType.LOG_IN);

                clientThread.send(msg);
                new Thread(clientThread).start();
                btnSend.setEnabled(true);
                lblStatus.setText("Connected to server");
                btnConnect.setEnabled(false);
                lbIcon.setText(from);
                setTitle("Welcome " + txtUsername.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_btnConnectActionPerformed


    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        /*insert code for sending a message to server here*/
        try {
            String from = txtUsername.getText();
            String to = clientThread.getServer().getHost();
            String content = txtMessage.getText();
            Message m = new Message(from, to, new Date(), content, MessageType.NORMAL);
            clientThread.send(m);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnSendActionPerformed

    private void miUpdateProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miUpdateProfileActionPerformed
        //
        try {
            if (clientThread != null) {
                ProfileBox pBox = new ProfileBox(this, false);

                ProfileThread pThread = new ProfileThread(clientThread.getServer(),
                    lbIcon);
                pThread.start();
                pBox.setHandlerThread(pThread, txtUsername.getText());
                pBox.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(btnConnect, "Connect to server first");
            }
        } catch (IOException ex) {
            Logger.getLogger(ClientBox.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_miUpdateProfileActionPerformed

