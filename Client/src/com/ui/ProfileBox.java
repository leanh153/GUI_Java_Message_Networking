    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        // TODO add your handling code here:

        JFileChooser jFileChooser = new JFileChooser(System.getProperty("user.home"));
        int returnOption = jFileChooser.showOpenDialog(this);
        if (returnOption == JFileChooser.APPROVE_OPTION) {
            File selectedFile = jFileChooser.getSelectedFile();
            try {
                if (new FileHelper().isSupportImage(selectedFile)) {
                    txtIconPath.setText(jFileChooser.getSelectedFile().getAbsolutePath());
                    file = jFileChooser.getSelectedFile();
                    btnSet.setEnabled(true);
                }
            } catch (IOException ex) {
                Logger.getLogger(ProfileBox.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }

        } else {
            btnSet.setEnabled(false);
        }


    }//GEN-LAST:event_btnSelectActionPerformed

    private void btnSetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetActionPerformed
        // TODO add your handling code here:
        try {

            if (file != null) {
                Message msg = new Message(userName, new Date(), file, MessageType.PROFILE);
                if (pThread != null) {
                    pThread.send(msg);
                }
            }
        } catch (IOException ex) {
            Logger.getLogger(ProfileBox.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSetActionPerformed

