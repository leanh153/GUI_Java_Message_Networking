    void setHandlerThread(ProfileThread pThread, String userName) {
        this.pThread = pThread;
        this.userName = userName;
        // this add abstractAction close then really close this profileBox
        // and when close event occure call setRunalble(false) to close thread
        // that handle this Jdrialog
         this.add(new JButton(new AbstractAction("Close") {
            @Override
            public void actionPerformed(ActionEvent e) {
                setVisible(false);
                dispatchEvent(new WindowEvent(
                    ProfileBox.this, WindowEvent.WINDOW_CLOSING));//To change body of generated methods, choose Tools | Templates.
            }

        }));

        this.addWindowListener(new WindowAdapter() {

            @Override
            public void windowClosing(WindowEvent e) {

                if (pThread != null) {
                    pThread.setRunnable(false);
                }

            }
        });
    }

